package myfirstpackage;

public class Car {

	public static void main(String[] args) {

		// multiple declaration assignment on same line

		int a = 10, b = 20, c = 30;

		String name1 = "Adam", name2 = "Jillian", name3 = "Odiljan";

		// making new line in print statement \n
		// AdamJillianOdiljan
		System.out.println(a + b + c);
		System.out.println(name1 + "\n" + name2 + "\n" + name3);

		// making new tab in print statement \t

		System.out.println(name1 + "\t" + name2 + "\t" + name3);

	}

}
