package day15;

public class Swapping {

	public static void main(String[] args) {
		// Task 1 :
		// create 2 int variables assign value
		// swap the value of these two variable
		// and print'em out
		
		// using temp variable is allowed
		
		
		int x = 10, y = 20;		 
		int temp;
		
		temp=x;
		x=y;
		y=temp;
		
		
	
		System.out.println("x is: "+x);
		System.out.println("y is: "+y);
		
		System.out.println(temp);
	}

}
