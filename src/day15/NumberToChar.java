package day15;

public class NumberToChar {

	public static void main(String[] args) {
		
		int x = 100;
		
		char myChar = (char)x;
		System.out.println(myChar);
		
		// the low to high order in ascii table
		// special character -> number -> upper-case -> lower-case
		
		double y = 100.99;
		char myChar2 = (char) y;
		System.out.println(myChar2);

	}

}
