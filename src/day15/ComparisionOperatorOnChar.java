package day15;

public class ComparisionOperatorOnChar {

	public static void main(String[] args) {
		
		// comparison/relation operator
		char c1 = 'a';
		char c2 = 'B';
		char c3 = ' ';
		System.out.println((int)c3);
		
		
		System.out.println(c1==c2);
		System.out.println(c1!=c2);
		System.out.println(c1>=c2);
		System.out.println(c1<c2);
		System.out.println(c1<=c2);
		
		int a = c1;
		int b = c2;
		System.out.println(a);
		System.out.println(b);

	}

}
