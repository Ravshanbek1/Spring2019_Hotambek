package day06;

public class ComparisionOperator {

	public static void main(String[] args) {
		
		// if you have 2 numbers how to compare them
		
		int a = 11, b = 11;
		
		// checking for equality --> ==
		System.out.println("is a and b equal: " +(a == b));
		
		// checking for inequality ---> \!=
		System.out.println("is a and b equal :"+(a != b));
		
		// checking for greater or equal ---> !=
		System.out.println("is a grreater or to equal : "+(a >= b));
				
		// checking for greater --> <=
		System.out.println("is a greater than b :"+ (a > b));
		
		// checking for less or equal --> <=
		System.out.println("is a less or equal to b :"+ (a <= b));
		
		// check for less than 
		System.out.println("is a less than b :"+ (a < b));
		
		//boolean isAequal = (a==b);

	}

}
